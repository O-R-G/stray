KNOCKING AROUND BETWEEN MONEY, SEX, AND BOREDOM: WALKER EVANS IN HAVANA AND NEW YORK<br>
<br>
John Tagg, Distinguished Professor of Art History, Binghamton University, State University of New York<br>
<br>
The title comes from a short memoir of his arrival in Havana written by Walker Evans in July 1933, after returning from a thirty-day trip to Cuba. Evans was recalling his first meeting with his guides, the Fernandez de Castro brothers, but his phrase encapsulates much about his own life at this time, raising issues invariably kept at bay in writing on Evans that have an undeniable if hard to focus relation to the understanding of his photographic work from this period. Moving between Havana and New York City, the lecture will ask not just about the passage between work and biography, but about sex and work and what is secured in the refusal to speak about them when looking at photographs by Evans.<br>
<br>
Born in the northeast of England and trained at the Royal College of Art in London, John Tagg writes on forms of photographic practice not previously considered part of the history of photography, including police and prison photography, social surveillance, urban records, and other archival systems in which the photograph is made to serve as a document. From here, his interests have extended to the ways we construct histories of cultural technologies and visual regimes, and to the theoretical debates that have transformed the history of art and photography since the 1970s. Tagg, the author of "The Burden of Representation" (1988) and "The Disciplinary Frame" (2009), among other books, is spending the fall semester at Yale as the Andrew Carnduff Ritchie visiting scholar.<br>
